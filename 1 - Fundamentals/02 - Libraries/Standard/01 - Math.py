"""
MATH — Python Standard Library

Provides mathematical functions such as:
- square roots
- trigonometry
- rounding
- constants (pi, e)
"""

import math

# ---------------------------------------------------------
# BASIC FUNCTIONS
# ---------------------------------------------------------

print(math.sqrt(25))
print(math.ceil(4.1))
print(math.floor(4.9))

# ---------------------------------------------------------
# TRIGONOMETRY
# ---------------------------------------------------------

print(math.sin(math.pi / 2))
print(math.cos(0))

# ---------------------------------------------------------
# CONSTANTS
# ---------------------------------------------------------

print(math.pi)
print(math.e)
